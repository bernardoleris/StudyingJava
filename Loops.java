public class Loops{
    public static void main(String[] args){
        for(int i = 0; i<=10; i++){
            System.out.println(i);
        }
        int numero = 0;
        while(numero<=10){
            System.out.println(numero);
            numero++;
        }
        numero = 0;
        do{
            System.out.println(numero);
            numero++;
        }while(numero<=10);
    }
}
